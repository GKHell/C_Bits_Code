

#include<stdio.h>
void main()
{
	int line;

	printf("Enter line no : ");
	scanf("%d",&line);

	switch(line)
	{
		case 1:printf("line 1\n");break;
		case 2:printf("line 2\n");break;
		case 3:printf("line 3\n");break;
		case 4:printf("line 4\n");break;
		case 5:printf("line 5\n");break;
		case 6:printf("line 6\n");break;
		case 7:printf("line 7\n");break;
		default:printf("invalid line code\n");

	}
	printf("hell");
}