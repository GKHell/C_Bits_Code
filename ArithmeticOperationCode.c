
// 4 + 2 
/*
	4,2 are operands 
	+ is operator
*/

#include<stdio.h>
int  main()
{
	int a,b;

	printf("Enter 2 numbers : ");
	scanf("%d,%d",&a,&b);
	printf("%d + %d = %d\n",a,b,a+b);
	printf("%d - %d = %d\n",a,b,a-b);
	printf("%d * %d = %d\n",a,b,a*b);
	printf("%d / %d = %d\n",a,b,a/b);
	printf("%d %% %d = %d\n",a,b,a%b);

	printf("\n\n-------------\\n-----------------");

}